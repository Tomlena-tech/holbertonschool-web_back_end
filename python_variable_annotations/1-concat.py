#!/usr/bin/env python3
"""Basic annotations - concat"""


def concat(str1: str, str2: str) -> str:
    """Concatene 2 chaines de caractères
    
    Args:
        str1: première chaîne
        str2: deuxième chaîne
    
    Returns:
        La concaténation de str1 et str2
    """
    return str1 + str2
